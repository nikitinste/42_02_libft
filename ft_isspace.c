#include "libft.h"

int		ft_isspace(int c)
{
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\r' \
		|| c == 32)
	{
		return (1);
	}
	else
		return (0);
	return (0);
}
